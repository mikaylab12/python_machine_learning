# Challenge 1
import numpy as np
x = np.arange(0,21) # generates a number from 0 up until 20
# calculating the mean
my_mean = np.mean(x)
print("The mean number is : ", my_mean)
# calculating the standard deviation
my_stdn = np.std(x)
print("The standard deviation  is :", my_stdn)
# calculatin the variance
my_var = np.var(x)
print("The variance is :", my_var)